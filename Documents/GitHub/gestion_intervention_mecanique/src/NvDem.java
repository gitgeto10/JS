    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      String machine = jMachine.getText();
      String probleme = jProbleme.getText();
      PreparedStatement pstmt = null;

        try {

            // Establish connection
             Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/gestion_mecanique?userSSL=false","root","");

            // SQL query
            String sql = "INSERT INTO interventions (machine, probleme, code_dem) VALUES (?, ?, ?)";

            // Préparation de la déclaration
            pstmt = con.prepareStatement(sql);
            pstmt.setString(1, machine);
            pstmt.setString(2, probleme);
            pstmt.setString(3, login.userCode);

            int rowsAffected = pstmt.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Intervention insérée avec succès", "Success", JOptionPane.INFORMATION_MESSAGE);
                dispose();
                HomeDem hpage = new HomeDem();
                hpage.show();
            } else {
                JOptionPane.showMessageDialog(null, "Échec d'insertion de cette intervention:", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Échec d'insertion de cette intervention:" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } 

    }//GEN-LAST:event_jButton2ActionPerformed

